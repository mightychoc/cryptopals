use super::super::traits::Alphabet;
use std::sync::OnceLock;

/// English letters-only alphabet used for letter frequency scoring.
/// The unigram and bigram frequencies are taken from [https://norvig.com/mayzner.html](https://norvig.com/mayzner.html)
pub struct EnglishAlphabet;

impl Alphabet for EnglishAlphabet {
    const SIZE: usize = 26;

    fn index(byte: u8) -> Option<usize> {
        match byte.to_ascii_lowercase() {
            b @ b'a'..=b'z' => Some((b - b'a') as usize),
            _ => None,
        }
    }

    fn unigram_probs() -> &'static [f64] {
        &ENGLISH_UNIGRAM // Your original const
    }

    fn bigram_probs() -> Option<&'static [f64]> {
        Some(&ENGLISH_BIGRAM)
    }

    fn unigram_log_probs() -> &'static [f64] {
        static CACHE: OnceLock<Vec<f64>> = OnceLock::new();
        CACHE.get_or_init(|| Self::unigram_probs().iter().map(|&p| p.ln()).collect())
    }

    fn bigram_log_probs() -> Option<&'static [f64]> {
        static CACHE: OnceLock<Option<Vec<f64>>> = OnceLock::new();

        let cached_option = CACHE.get_or_init(|| {
            Self::bigram_probs().map(|table| table.iter().map(|&p| p.ln()).collect())
        });

        cached_option.as_deref()
    }
}

